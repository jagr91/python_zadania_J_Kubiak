# Napisz program, który ilistruje działanie operatora logicznego and

a = True
b = True

print('Działanie operatora and:')
print(f'a = True, b = True => {a and b}')
print(f'a = True, b = False => {a and not b}')
print(f'a = False, b = True => {not a and b}')
print(f'a = False, b = False => {not a and not b}')
